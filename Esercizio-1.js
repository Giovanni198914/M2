//Cos'e un algoritmo?
//E' un insieme di step necessari alla risoluzioni di un problema. Attraverso vari passaggi (input) determino il risultato finale (output)

//Cos'e una variabile?
//Sono dei contenitori per dati che vengono elaborati parzialmente. La variabile va dichiarata in quanto bisogna assegnarle un valore.

//Undefined e null sono la stessa cosa?
//Undefined rappresenta un valore al momento non esiste. Alla variabile non e' stato assegnato alcun valore.
//Null rappresenta un valore vuoto in quanto la variabile non contiene nulla.